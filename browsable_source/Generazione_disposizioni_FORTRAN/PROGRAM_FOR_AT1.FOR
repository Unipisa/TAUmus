TEXT PROGRAM FOR AT/1
   DIMENSION FR(288),NP(4),KFR(288)
   WRITE(6,1)
1  FORMAT(LX,0BATTI UN NUMERO DI 8 CIFRE0)
   READ(5,2)N1,N2
2  FORMAT (2I4)
   N=0
   N=N+1
   N2=N2*233
   N1=(N1*23)+(N2/10000)
   N2=IABS(MOD(N2,10000)-(N1/10000))
   IF(N2.LE.0)N2=101
   N1=MOD(N1,10000)
   IF(N1.LE.0)N1=101
   NP(1)=N1/100
   NP(2)=MOD(N1,100)
   NP(3)=N2/100
   NP(4)=MOD(N2,100)
   FR(N)=FLOAT(NP(1)*FLOAT(NP(2))/2.+30.
   IF(N.EQ.288) GO TO 20
   GO TO 10
20 WRITE(6,21)FR
21 FORMAT(1X,8(F10.3,4X))
   N=O
30 N=N+1
   X=1.
31 X=X*2.
   IF(FR(N).LE.(22.*X))GO TO 32
   GO TO 31
32 XFR=22.*X
   K=-1
   RX=2.**(1./36.)
33 K=K+1
   R=RX**K
   YFR=XFR/R
   IF(FR(N).GE.YFR)GO TO 34
   GO TO 33
34 IF(((YFR*RX)/FR(N).LT.(FR(N)/YFR))GO TO 35
   FR(N)=YFR
   KFR(N)=K
   GO TO 36
35 FR(N)=YFR*RX
   KFR(N)=K-1
36 IF(N.GE.288)GO TO 40
   GO TO 30
40 WRITE(6,50)(KFR(J),FR(J),J=1,288)
50 FORMAT(1X,5(I2,3X,F10.3,4X))
   STOP
   END
